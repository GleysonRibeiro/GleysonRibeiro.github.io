
public class Subtracao {
    
    double direita, esquerda;
    
    public Subtracao(double direita, double esquerda){
        this.direita=direita;
        this.esquerda=esquerda;
    }
    
    
    public double avaliar(){
        
        return this.direita - this.esquerda;    
    }
    
    
}
